					<h2 id="musc">Muscualação</h2>
					<p>O treinamento de força, conhecido popularmente como musculação é uma forma de exercício contra resistência, praticado normalmente em ginásios, para o treinamento e desenvolvimento dos músculos esqueléticos. Utiliza a força da gravidade (com barras, halteres, pilhas de peso ou o peso do próprio corpo) e a resistência gerada por equipamentos, elásticos e molas para opor forças aos músculos que, por sua vez, devem gerar força oposta através de contrações musculares que podem ser concêntricas, excêntricas e isométricas.</p>
					<h2 id="fun">Funcional</h2>
					<p>A principal característica desse tipo de treinamento são os exercícios focados na necessidade de quem pratica. Ou seja, eles são elaborados para alcançar o resultado que o aluno precisa, seja fortalecimento ou queima de calorias. Assim, o indivíduo pratica diferentes sequências em cima de bases instáveis, como bolas, pranchas e bastões, e aciona os músculos e articulações de forma global, desenvolvendo coordenação motora, flexibilidade, agilidade, equilíbrio, força e aptidão cardiorrespiratória.</p>
					<h2 id="pil">Pilates</h2>
					<p>Pilates é um método de exercício físico inicialmente desenvolvido durante a primeira guerra mundial (1914 – 1918) para o ganho de força muscular, mas que hoje é usado para diversos fins, como o ganho de equilíbrio, melhora da flexibilidade e reabilitação.</p>
					
					
